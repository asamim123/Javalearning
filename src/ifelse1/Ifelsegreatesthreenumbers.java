package ifelse1;

public class Ifelsegreatesthreenumbers {

	public static void main(String[] args) {
		// If else Greater three numbers

		int a = 10, b = 100, c = 20;
		if (a > b && a > c) {
			System.out.println("a is greater");
		} else if (b > a && b > c) {
			System.out.println("b is greater");

		} else {
			System.out.println("c is greater");
		}

	}

}
