package ifelse1;

public class Ifelse3 {

	public static void main(String[] args) {
		int a = 50;
		int b = 60;
		if (a > b) {
			System.out.println("a is greater ");
		} else {
			System.out.println("b is greater ");
		}

		if (a < b) {
			System.out.println("a is less ");
		} else {
			System.out.println("b is greater");
		}

	}

}
